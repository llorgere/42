int		ft_iterative_factorial(int nb)
{
	int		i;
	int		f;

	i = 1;
	f = 1;
	if (nb < 13 && nb >= 0)
	{
		while (i <= nb)
		{
			f = i * f;
			i++;
		}
		return (f);
	}
	else
		return (0);
}
